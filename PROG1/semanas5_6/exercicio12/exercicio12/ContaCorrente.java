public class ContaCorrente{
    private int conta;
    private double saldo;
    
    public int getConta(){
        return conta;
    }
    
    public double getSaldo(){
        return saldo;
    }
    
    public void setConta(int conta){
        this.conta = conta;
    }
    
    public void setSaldo(double saldo){
        this.saldo = saldo;
    }
}