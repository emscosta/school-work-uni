public class Computador{
    private String marca;
    private double velocidade;
    private int ano;
    private char estadoPC;
    
    public Computador(String marca, double velocidade, int ano, char estadoPC){
        this.marca = marca;
        this.velocidade = velocidade;
        this.ano = ano;
        this.estadoPC = estadoPC;
        
    }
}